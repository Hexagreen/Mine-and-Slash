public class MobChillEffect extends BasePotionEffect {

    public static final MobChillEffect INSTANCE = new MobChillEffect();

    private MobChillEffect() {
        super(EffectType.HARMFUL, 4393423);
        this.setRegistryName(new ResourceLocation(Ref.MODID, GUID()));

        this.addAttributesModifier(SharedMonsterAttributes.MOVEMENT_SPEED, "7107DE5E-7CE8-4030-940E-514C1F160890",
                (double) -0.33F, AttributeModifier.Operation.MULTIPLY_BASE
        );

        this.tickActions.add(new OnTickAction(ctx -> {
            ParticleUtils.spawnParticles(ParticleTypes.ITEM_SNOWBALL, ctx.entity, 25);
            return ctx;
        }, null));
    }

    @Override
    public String GUID() {
        return "mob_chill_effect";
    }

    @Override
    public String locNameForLangFile() {
        return "Mob Chill";
    }

    @Override
    public List<ITextComponent> getEffectTooltip(TooltipInfo info) {
        return new ArrayList<>();
    }

    @Override
    public int getMaxStacks() {
        return 1;
    }

    @Override
    public PreCalcSpellConfigs getPreCalcConfig() {
        PreCalcSpellConfigs p = new PreCalcSpellConfigs();
        p.set(SC.TICK_RATE, 20, 20);
        p.set(SC.DURATION_TICKS, 60, 60);
        return p;
    }

    @Nullable
    @Override
    public BaseSpell getSpell() {
        return null;
    }

    @Override
    public Masteries getMastery() {
        return null;
    }
}
